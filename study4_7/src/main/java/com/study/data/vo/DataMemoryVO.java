package com.study.data.vo;

import java.io.Serializable;

import org.apache.commons.lang3.builder.ToStringBuilder;
import org.apache.commons.lang3.builder.ToStringStyle;

public class DataMemoryVO implements Serializable {
	
	private String id;           /* 아이디 */
	private String name;         /* 이름 */
	private String memoryCategory;     /* 카테고리 */
	private String year;         /* 출시년도 */
	private int price;           /* 가격 */
	private String img;          /* 이미지 */
	private String url;          /* url */
	private String memoryMaker;        /* 제조사 */
	private String memoryDevice;       /* 사용장치 */
	private String memoryType;         /* 제품 분류 */
	private String memoryStandard;     /* 메모리규격 */
	private String memoryCapacity;     /* 메모리 용량 */
	private String memoryClock;        /* 동작클럭 */
	private String memoryTiming;       /* 램타이밍 */
	private String memoryV;            /* 동작전압 */
	private String memoryCnt;          /* 램개수 */
	private String memoryAdd;          /* 부가기능 */
	private String memorySync;         /* 히트싱크 */
	private String memoryColor;        /* 방열판 색상 */
	
	@Override
	public String toString() {
		return ToStringBuilder.reflectionToString(this, ToStringStyle.MULTI_LINE_STYLE);
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getMemoryCategory() {
		return memoryCategory;
	}

	public void setMemoryCategory(String memoryCategory) {
		this.memoryCategory = memoryCategory;
	}

	public String getYear() {
		return year;
	}

	public void setYear(String year) {
		this.year = year;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public String getImg() {
		return img;
	}

	public void setImg(String img) {
		this.img = img;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getMemoryMaker() {
		return memoryMaker;
	}

	public void setMemoryMaker(String memoryMaker) {
		this.memoryMaker = memoryMaker;
	}

	public String getMemoryDevice() {
		return memoryDevice;
	}

	public void setMemoryDevice(String memoryDevice) {
		this.memoryDevice = memoryDevice;
	}

	public String getMemoryType() {
		return memoryType;
	}

	public void setMemoryType(String memoryType) {
		this.memoryType = memoryType;
	}

	public String getMemoryStandard() {
		return memoryStandard;
	}

	public void setMemoryStandard(String memoryStandard) {
		this.memoryStandard = memoryStandard;
	}

	public String getMemoryCapacity() {
		return memoryCapacity;
	}

	public void setMemoryCapacity(String memoryCapacity) {
		this.memoryCapacity = memoryCapacity;
	}

	public String getMemoryClock() {
		return memoryClock;
	}

	public void setMemoryClock(String memoryClock) {
		this.memoryClock = memoryClock;
	}

	public String getMemoryTiming() {
		return memoryTiming;
	}

	public void setMemoryTiming(String memoryTiming) {
		this.memoryTiming = memoryTiming;
	}

	public String getMemoryV() {
		return memoryV;
	}

	public void setMemoryV(String memoryV) {
		this.memoryV = memoryV;
	}

	public String getMemoryCnt() {
		return memoryCnt;
	}

	public void setMemoryCnt(String memoryCnt) {
		this.memoryCnt = memoryCnt;
	}

	public String getMemoryAdd() {
		return memoryAdd;
	}

	public void setMemoryAdd(String memoryAdd) {
		this.memoryAdd = memoryAdd;
	}

	public String getMemorySync() {
		return memorySync;
	}

	public void setMemorySync(String memorySync) {
		this.memorySync = memorySync;
	}

	public String getMemoryColor() {
		return memoryColor;
	}

	public void setMemoryColor(String memoryColor) {
		this.memoryColor = memoryColor;
	}
	
	
}
